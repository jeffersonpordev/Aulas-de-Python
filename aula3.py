"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Jefferson')
print(1, 'Jefferson da "Silva"')


# Aspas duplas
print('Jefferson da "Silva"')
print("Jefferson da Silva")

# Escape
print(2, "Jefferson da \'Silva\'")

# r
print(r"Jefferson da \"Silva\"")


